/* npm (Node Package Manager):

npm is primarily used for managing packages and dependencies for Node.js projects.
It allows you to install, uninstall, update, and manage packages for your project.
It also provides functionality for managing project scripts, versioning, and publishing packages to the npm registry.
npm install <package-name>

*/

/* npx (Node Package Executer):
npx is a tool that comes with npm (version 5.2.0 and above) which allows you to execute Node.js packages without installing them globally or locally.
It is mainly used for running binaries from npm packages without needing to install them globally or specify their full path.
It also helps in executing packages with specific versions, avoiding conflicts with globally installed packages.
npx create-react-app my-react-app
*/

